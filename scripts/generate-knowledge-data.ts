import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// ãƒ—ãƒ­ã‚¸ã‚§ã‚¯ãƒˆãƒ«ãƒ¼ãƒˆã¨ã‚½ãƒ¼ã‚¹ãƒ‡ã‚£ãƒ¬ã‚¯ãƒˆãƒªã®ãƒ‘ã‚¹
const projectRoot = join(__dirname, '..');
const knowledgeDir = join(projectRoot, 'src', 'knowledge');
const outputFile = join(knowledgeDir, 'data.ts');

// å¤‰æ›å¯¾è±¡ã®markdownãƒ•ã‚¡ã‚¤ãƒ«
const knowledgeFiles = [
  'bidding-methods.md',
  'evaluation.md',
  'glossary.md',
  'investigation-primer.md',
  'legal-constraints.md',
  'qualifications.md',
  'search-strategies.md',
  'timelines.md',
];

/**
 * æ–‡å­—åˆ—ã‚’TypeScriptã®æ–‡å­—åˆ—ãƒªãƒ†ãƒ©ãƒ«ã¨ã—ã¦ã‚¨ã‚¹ã‚±ãƒ¼ãƒ—
 */
function escapeForTypeScript(content: string): string {
  // ãƒãƒƒã‚¯ã‚¯ã‚©ãƒ¼ãƒˆã‚’ã‚¨ã‚¹ã‚±ãƒ¼ãƒ—
  return content.replace(/`/g, '\\`').replace(/\$/g, '\\$');
}

/**
 * markdownãƒ•ã‚¡ã‚¤ãƒ«ã‚’èª­ã¿è¾¼ã‚“ã§TypeScriptã‚³ãƒ¼ãƒ‰ã‚’ç”Ÿæˆ
 */
function generateKnowledgeData(): void {
  console.log('ğŸ“š Generating knowledge data...');

  const dataMap: Record<string, string> = {};

  // å„markdownãƒ•ã‚¡ã‚¤ãƒ«ã‚’èª­ã¿è¾¼ã‚€
  for (const filename of knowledgeFiles) {
    const filepath = join(knowledgeDir, filename);
    try {
      const content = readFileSync(filepath, 'utf-8');
      dataMap[filename] = content;
      console.log(`  âœ“ Loaded ${filename} (${content.length} bytes)`);
    } catch (error) {
      console.error(`  âœ— Failed to load ${filename}:`, error);
      process.exit(1);
    }
  }

  // TypeScriptã‚³ãƒ¼ãƒ‰ã‚’ç”Ÿæˆ
  const lines: string[] = [
    '/**',
    ' * Auto-generated knowledge data',
    ' * DO NOT EDIT MANUALLY',
    ' * Generated by scripts/generate-knowledge-data.ts',
    ' */',
    '',
    '/**',
    ' * Knowledge data map: filename -> markdown content',
    ' */',
    'export const KNOWLEDGE_DATA: Record<string, string> = {',
  ];

  for (const [filename, content] of Object.entries(dataMap)) {
    const escaped = escapeForTypeScript(content);
    lines.push(`  '${filename}': \`${escaped}\`,`);
  }

  lines.push('};');
  lines.push('');

  const output = lines.join('\n');

  // ãƒ•ã‚¡ã‚¤ãƒ«ã«æ›¸ãè¾¼ã‚€
  try {
    writeFileSync(outputFile, output, 'utf-8');
    console.log(`\nâœ… Generated ${outputFile}`);
    console.log(`   Total files: ${knowledgeFiles.length}`);
    console.log(`   Total size: ${output.length} bytes`);
  } catch (error) {
    console.error('Failed to write output file:', error);
    process.exit(1);
  }
}

// ã‚¹ã‚¯ãƒªãƒ—ãƒˆå®Ÿè¡Œ
